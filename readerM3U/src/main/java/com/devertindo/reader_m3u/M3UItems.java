package com.devertindo.reader_m3u;

public class M3UItems {
   private String ext_http;
   private String manifest_type;
   private String http_user_agent;
   private String http_referrer;
   private String license_type;
   private String license_key;
   private String tvg_id;
   private String tvg_logo;
   private String group_title;
   private String title;
   private String url;

   public M3UItems() {
   }

   public String getExt_http() {
      return ext_http;
   }

   public void setExt_http(String ext_http) {
      this.ext_http = ext_http;
   }

   public String getManifest_type() {
      return manifest_type;
   }

   public void setManifest_type(String manifest_type) {
      this.manifest_type = manifest_type;
   }

   public String getHttp_user_agent() {
      return http_user_agent;
   }

   public void setHttp_user_agent(String http_user_agent) {
      this.http_user_agent = http_user_agent;
   }

   public String getHttp_referrer() {
      return http_referrer;
   }

   public void setHttp_referrer(String http_referrer) {
      this.http_referrer = http_referrer;
   }

   public String getLicense_type() {
      return license_type;
   }

   public void setLicense_type(String license_type) {
      this.license_type = license_type;
   }

   public String getLicense_key() {
      return license_key;
   }

   public void setLicense_key(String license_key) {
      this.license_key = license_key;
   }

   public String getTvg_id() {
      return tvg_id;
   }

   public void setTvg_id(String tvg_id) {
      this.tvg_id = tvg_id;
   }

   public String getTvg_logo() {
      return tvg_logo;
   }

   public void setTvg_logo(String tvg_logo) {
      this.tvg_logo = tvg_logo;
   }

   public String getGroup_title() {
      return group_title;
   }

   public void setGroup_title(String group_title) {
      this.group_title = group_title;
   }

   public String getTitle() {
      return title;
   }

   public void setTitle(String title) {
      this.title = title;
   }

   public String getUrl() {
      return url;
   }

   public void setUrl(String url) {
      this.url = url;
   }
}
